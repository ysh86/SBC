enum {
    FBKB_SCAN_BYTES = 9,
    FBKB_SCAN_REPEAT_COUNT = 3,
    FBKB_PORT_COUNT = 2,
    FBKB_PORT_4016 = 0,
    FBKB_PORT_4017 = 1,
    FBKB_OUT0_ROW_RESET = 1u << 0,
    FBKB_OUT1_SELECT_H = 1u << 1,
    FBKB_OUT2_SELECT_KEYBOARD = 1u << 2,
    FBKB_WRITE_COMMAND = 0x50000000u,
    FBKB_COMMAND_PORT_SHIFT = 8,
};

struct FBKB {
    uint8_t row_index;
    uint8_t scan_cycle;
    uint8_t scan_code_l[FBKB_SCAN_BYTES];
    uint8_t scan_code_h[FBKB_SCAN_BYTES];
    uint8_t read_data[FBKB_PORT_COUNT];
    uint8_t write_data[FBKB_PORT_COUNT];
};

static volatile struct FBKB fbkb;

static inline __attribute__((always_inline)) uint32_t fbkb_make_write_command(uint8_t index, uint8_t value) {
    return FBKB_WRITE_COMMAND | ((uint32_t)index << FBKB_COMMAND_PORT_SHIFT) | value;
}

static inline __attribute__((always_inline)) void fbkb_update_read_data(void) {
    // not implemented yet
    //fbkb.read_data[FBKB_PORT_4016] = 0;

    if ((fbkb.write_data[FBKB_PORT_4016] & FBKB_OUT2_SELECT_KEYBOARD) == 0u) {
        fbkb.read_data[FBKB_PORT_4017] = 0;
    } else if ((fbkb.write_data[FBKB_PORT_4016] & FBKB_OUT1_SELECT_H) != 0u) {
        fbkb.read_data[FBKB_PORT_4017] = fbkb.scan_code_h[fbkb.row_index];
    } else {
        fbkb.read_data[FBKB_PORT_4017] = fbkb.scan_code_l[fbkb.row_index];
    }
}

static inline __attribute__((always_inline)) void fbkb_load_scan_code(uint8_t code) {
    for (uint8_t i = 0; i < FBKB_SCAN_BYTES; ++i) {
        uint8_t row_code = scanTable[code][i];
        fbkb.scan_code_l[i] = (uint8_t)((~row_code & 0x0fu) << 1);
        fbkb.scan_code_h[i] = (uint8_t)((~row_code & 0xf0u) >> 3);
    }
}

static inline __attribute__((always_inline)) void fbkb_write_register(uint8_t index, uint8_t value) {
    bool is_RESET = value & FBKB_OUT0_ROW_RESET;
    bool prev_is_H = fbkb.write_data[FBKB_PORT_4016] & FBKB_OUT1_SELECT_H;
    bool curr_is_H = value & FBKB_OUT1_SELECT_H;

    fbkb.write_data[index] = value;
    if (index == FBKB_PORT_4017 || (value & FBKB_OUT2_SELECT_KEYBOARD) == 0) {
        return;
    }

    if (is_RESET) {
        fbkb.row_index = 0;
        if (fbkb.scan_cycle == 0u) {
            int c = getchar_timeout_us(0);
            if (c == PICO_ERROR_TIMEOUT) {
                fbkb_load_scan_code(0u);
            } else {
                fbkb_load_scan_code((uint8_t)c);
                fbkb.scan_cycle = 1;
            }
        } else if (fbkb.scan_cycle < FBKB_SCAN_REPEAT_COUNT) {
            ++fbkb.scan_cycle;
        } else if (fbkb.scan_cycle == FBKB_SCAN_REPEAT_COUNT) {
            fbkb_load_scan_code(0u);
            ++fbkb.scan_cycle;
        } else if (++fbkb.scan_cycle == FBKB_SCAN_REPEAT_COUNT * 2u) {
            fbkb.scan_cycle = 0;
        }
    } else if (prev_is_H && !curr_is_H) {
        ++fbkb.row_index;
        if (fbkb.row_index == FBKB_SCAN_BYTES) fbkb.row_index = 0;
    }

    fbkb_update_read_data();
}
